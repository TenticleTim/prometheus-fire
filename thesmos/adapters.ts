export function generateClaudeRules(rules: Rule[], config: ThesmosConfig): string {
  // Only BLOCKER and HIGH are embedded inline — MEDIUM/LOW/TECH_DEBT live in .thesmos/RULES.md.
  // Embedding all severities causes context thrashing: 450+ rules × 3 columns = 1150+ lines
  // loaded on every turn. MEDIUM/LOW rules are advisory; agents can pull them on demand.
  const CLAUDE_SEVERITIES: Severity[] = ['BLOCKER', 'HIGH'];
  const grouped = new Map<Severity, Rule[]>();
  for (const sev of CLAUDE_SEVERITIES) grouped.set(sev, []);
  for (const rule of rules) {
    if (CLAUDE_SEVERITIES.includes(rule.severity)) {
      grouped.get(rule.severity)!.push(rule);
    }
  }

  const parts: string[] = [
    `_Generated by Thesmos ${config.version} for **${config.project}**. Full rule details in \`.thesmos/governance/CODE_REVIEW.md\`._`,
    '',
    '## Enforced Rules',
    '',
    '> **BLOCKER and HIGH only.** MEDIUM / LOW / TECH_DEBT rules are in `.thesmos/RULES.md` — not embedded here to prevent context thrashing.',
    '',
  ];

  for (const sev of CLAUDE_SEVERITIES) {
    const group = grouped.get(sev)!;
    if (group.length === 0) continue;
    parts.push(`### ${SEVERITY_EMOJI[sev]} ${sev}`, '');
    parts.push('| Rule | Category | Summary |');
    parts.push('|---|---|---|');
    for (const rule of group) {
      // Truncate description to keep the table scannable; full text is in CODE_REVIEW.md
      const summary = rule.description.length > 80
        ? rule.description.slice(0, 77) + '…'
        : rule.description;
      parts.push(`| [${rule.id}] | \`${rule.category}\` | ${summary} |`);
    }
    parts.push('');
  }

  parts.push(
    `_Run \`npm run thesmos:review\` for findings. Run \`npm run thesmos:validate\` to gate CI._`
  );

  parts.push('', generatePantheonProtocol());

  return parts.join('\n').trimEnd();
}

