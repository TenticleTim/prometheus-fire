// ── Filter helpers (pure) ─────────────────────────────────────────────────────

export function getRulesByTag(rules: Rule[], tag: string): Rule[] {
  return rules.filter((r) => r.tags.includes(tag));
}

export function getRulesBySeverity(rules: Rule[], severity: Severity): Rule[] {
  return rules.filter((r) => r.severity === severity);
}

export function getRulesByCategory(rules: Rule[], categories: string[]): Rule[] {
  const set = new Set(categories);
  return rules.filter((r) => set.has(r.category));
}

// ── Shared formatting (pure) ──────────────────────────────────────────────────

function formatRulesSections(rules: Rule[]): string {
  const grouped = new Map<Severity, Rule[]>();
  for (const sev of SEVERITY_ORDER) grouped.set(sev, []);
  for (const rule of rules) grouped.get(rule.severity)!.push(rule);

  let md = '';
  for (const sev of SEVERITY_ORDER) {
    const group = grouped.get(sev)!;
    if (group.length === 0) continue;
    md += `\n### ${SEVERITY_EMOJI[sev]} ${sev}\n\n`;
    for (const rule of group) {
      md += `**[${rule.id}]** ${rule.description}\n`;
      if (rule.example) {
        md += `\n\`\`\`ts\n${rule.example}\n\`\`\`\n`;
      }
      md += '\n';
    }
  }
  return md.trimEnd();
}

// ── Per-target generators (pure) ──────────────────────────────────────────────

export function generateGeminiRules(rules: Rule[], config: ThesmosConfig): string {
  return [
    `_Generated by Thesmos ${config.version} for **${config.project}**._`,
    '',
    'The following rules are enforced during code review. Violations marked **BLOCKER** must be fixed before merge.',
    formatRulesSections(rules),
  ].join('\n');
}

