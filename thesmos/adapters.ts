// ── Types ─────────────────────────────────────────────────────────────────────

export type AdapterTarget =
  | 'gemini'
  | 'claude'
  | 'cursor'
  | 'copilot'
  | 'codex'
  | 'agents';

export interface Rule {
  id: string;
  category: string;
  description: string;
  severity: Severity;
  tags: string[];
  example?: string;
}

export interface AdapterManifest {
  target: AdapterTarget;
  outputPath: string;
  generated: boolean;
}

