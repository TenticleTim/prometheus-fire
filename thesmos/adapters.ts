/**
 * Thesmos adapter system — generates AI assistant instruction files
 * from a shared canonical rule registry. All generator functions are pure.
 *
 * Supported targets and their output paths:
 *   gemini  → GEMINI.md
 *   claude  → CLAUDE.md
 *   cursor  → .cursor/rules/thesmos.mdc
 *   copilot → .github/copilot-instructions.md
 *   codex   → .codex/thesmos.md
 *   agents  → AGENTS.md
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
