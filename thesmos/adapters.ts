// ── Adapter metadata ──────────────────────────────────────────────────────────

/** Metadata embedded inside every generated adapter section. No timestamps. */
export interface AdapterMeta {
  version: string;
  target: AdapterTarget;
  ruleCount: number;
}

/** Minimal catalog context passed to adapter builders — avoids importing catalog.ts. */
export interface AdapterCatalogEntry {
  id: string;
  name: string;
}

export interface AdapterCatalog {
  agents: AdapterCatalogEntry[];
  skills: AdapterCatalogEntry[];
  profile?: string;
}

function formatCatalogContext(catalog: AdapterCatalog): string {
  if (catalog.agents.length === 0 && catalog.skills.length === 0 && !catalog.profile) return '';
  const lines: string[] = ['', '---', '', '## Active Thesmos Context', ''];
  if (catalog.profile) lines.push(`**Profile:** \`${catalog.profile}\``, '');
  if (catalog.agents.length > 0) {
    lines.push('**Active Agents:**', '');
    for (const a of catalog.agents) lines.push(`- **${a.name}** (\`${a.id}\`) — Invoke: \`Agent({ subagent_type: "${a.id}", prompt: "..." })\``);
    lines.push('');
  }
  if (catalog.skills.length > 0) {
    lines.push('**Active Skills:**', '');
    for (const s of catalog.skills) lines.push(`- **[${s.id}]** ${s.name}`);
    lines.push('');
  }
  lines.push('_Run `thesmos catalog:list` to see all available agents and skills._');
  return lines.join('\n');
}

/**
 * Parse the THESMOS:META comment from an adapter file.
 * Returns null when the comment is absent or malformed.
 */
export function parseAdapterMeta(content: string): AdapterMeta | null {
  const match = content.match(/<!--\s*THESMOS:META\s+(\{[^}]+\})\s*-->/);
  if (!match || !match[1]) return null;
  try {
    return JSON.parse(match[1]) as AdapterMeta;
  } catch {
    return null;
  }
}

/**
 * Returns whether an adapter file's embedded metadata is current.
 * Freshness is checked by ruleCount and version only — no timestamps.
 */
export function isAdapterFresh(
  content: string,
  rules: Rule[],
  config: ThesmosConfig
): { fresh: boolean; reason: string } {
  const meta = parseAdapterMeta(content);
  if (!meta) {
    return { fresh: false, reason: 'no metadata — run thesmos adapters to refresh' };
  }
  if (meta.ruleCount !== rules.length) {
    return {
      fresh: false,
      reason: `rule count mismatch: file has ${meta.ruleCount}, registry has ${rules.length}`,
    };
  }
  if (meta.version !== config.version) {
    return {
      fresh: false,
      reason: `version mismatch: file has ${meta.version}, current is ${config.version}`,
    };
  }
  return { fresh: true, reason: 'up to date' };
}

// ── Public pure merger ────────────────────────────────────────────────────────

/**
 * Merges a freshly generated rules section into an existing adapter document.
 * If the document is empty, uses the target-specific preamble as the base.
 * Idempotent: calling twice with identical input produces identical output.
 *
 * The generated section always begins with a THESMOS:META comment so that
 * isAdapterFresh() can verify the file without re-running the generator.
 */
export function buildAdapterContent(
  target: AdapterTarget,
  existing: string,
  rules: Rule[],
  config: ThesmosConfig,
  catalog?: AdapterCatalog
): string {
  const base =
    existing.trim() === '' ? ADAPTER_PREAMBLES[target](config) : existing;
  const meta: AdapterMeta = { version: config.version, target, ruleCount: rules.length };
  const metaComment = `<!-- THESMOS:META ${JSON.stringify(meta)} -->`;
  const catalogCtx = catalog ? formatCatalogContext(catalog) : '';
  const generated = `${metaComment}\n${RULE_GENERATORS[target](rules, config)}${catalogCtx}`;
  return injectGeneratedSection(base, 'rules', generated);
}

// ── I/O orchestrator ──────────────────────────────────────────────────────────

/**
 * Writes adapter files for the requested targets under `root`.
 * Existing files are updated in-place (generated section only);
 * manually written content outside the markers is preserved.
 */
export function writeAllAdapters(
  root: string,
  rules: Rule[],
  config: ThesmosConfig,
  targets: AdapterTarget[] = [
    'gemini',
    'claude',
    'cursor',
    'copilot',
    'codex',
    'agents',
  ],
  catalog?: AdapterCatalog
): AdapterManifest[] {
  // Auto-generate context snapshot alongside adapters so CLAUDE.md step 1 always has fresh data
  try {
    const capsule = generateContextCapsule(root);
    saveContextCapsule(root, capsule);
  } catch { /* non-fatal — context snapshot is advisory */ }

  return targets.map((target) => {
    const relPath = ADAPTER_OUTPUT_PATHS[target];
    const absPath = join(root, relPath);
    const existing = existsSync(absPath) ? readFileSync(absPath, 'utf8') : '';
    const content = buildAdapterContent(target, existing, rules, config, catalog);
    const dir = dirname(absPath);
    if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
    writeFileSync(absPath, content, 'utf8');
    return { target, outputPath: relPath, generated: true };
  });
}
