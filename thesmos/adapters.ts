export function generateAgentsRules(rules: Rule[], config: ThesmosConfig): string {
  const blockers = getRulesBySeverity(rules, 'BLOCKER');
  const highs = getRulesBySeverity(rules, 'HIGH');
  const rest = rules.filter((r) => r.severity !== 'BLOCKER' && r.severity !== 'HIGH');

  const parts: string[] = [
    `_Generated by Thesmos ${config.version} for **${config.project}**._`,
    '',
  ];

  if (blockers.length > 0) {
    parts.push('**CRITICAL — These rules must never be violated. Stop and report before proceeding.**', '');
    for (const rule of blockers) {
      parts.push(`- **[${rule.id}]** ${rule.description}`);
    }
    parts.push('');
  }

  if (highs.length > 0) {
    parts.push('**HIGH PRIORITY — Must fix before task completion.**', '');
    for (const rule of highs) {
      parts.push(`- **[${rule.id}]** ${rule.description}`);
    }
    parts.push('');
  }

  if (rest.length > 0) {
    parts.push('**GUIDELINES**', '');
    for (const rule of rest) {
      parts.push(`- **[${rule.id}]** (${rule.severity}) ${rule.description}`);
    }
  }

  return parts.join('\n').trimEnd();
}

